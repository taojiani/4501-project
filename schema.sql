
CREATE TABLE IF NOT EXISTS nyc_zipcodes (
    zipcodes_id SERIAL PRIMARY KEY,
    zipcode INTEGER,
    po_name VARCHAR(255),
    population FLOAT,
    area FLOAT,
    state VARCHAR(255),
    county VARCHAR(255),
    geometry GEOMETRY(POLYGON,2263)
)

CREATE TABLE IF NOT EXISTS nyc_311 (
    complaints_id SERIAL PRIMARY KEY,
    unique_key VARCHAR(255),
    created_date TIMESTAMP,
    agency VARCHAR(255),
    complaint_type VARCHAR(255),
    descriptor VARCHAR(255),
    location_type VARCHAR(255),
    incident_zip INTEGER,
    city VARCHAR(255),
    borough VARCHAR(255),
    latitude FLOAT,
    longitude FLOAT
)

CREATE TABLE IF NOT EXISTS nyc_trees (
    trees_id SERIAL PRIMARY KEY,
    tree_id INTEGER,
    block_id INTEGER,
    status VARCHAR(255),
    address VARCHAR(255),
    zipcode VARCHAR(255),
    zip_city VARCHAR(255),
    latitude FLOAT,
    longitude FLOAT,
    x_sp FLOAT,
    y_sp FLOAT,
    health VARCHAR(255),
    spc_common VARCHAR(255),
    geometry GEOMETRY(Point, 4326)
 
)

